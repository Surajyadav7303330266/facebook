  var ukeVid1 = document.getElementById('uke1');

// init controller
var controller = new ScrollMagic.Controller();

// build scene
var scene = new ScrollMagic.Scene({triggerElement: "#uke1", duration: 200})
        .addTo(controller)
        //.addIndicators() // add indicators (requires plugin)

        .on("enter", function () {
          ukeVid1.play();
        })
        .on("leave", function () {
          ukeVid1.pause();
        })
